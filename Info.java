
class Info {

	private String isi;
	private int index;

	public Info(String isi)
	{
		this.isi = isi;
	}

	public Info(String isi, int index)
	{
		this.isi = isi;
		this.index = index;
	}

	public String getIsi()
	{
		return isi;
	}

	public int getIndex()
	{
		return index;
	}

}